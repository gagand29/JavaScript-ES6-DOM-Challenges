// Function to say hello
function sayHello(name) {
    // Log greeting message to the console
    console.log("Hello, " + name + "!");
    // Display greeting message in an alert dialog
    alert("Hello, " + name + "!");
}

// Function to say goodbye
function sayGoodbye(name) {
    // Log farewell message to the console
    console.log("Goodbye, " + name + "!");
    // Display farewell message in an alert dialog
    alert("Goodbye, " + name + "!");
}
